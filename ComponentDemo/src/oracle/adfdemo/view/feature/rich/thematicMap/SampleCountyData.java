    public void init (String state[]) {        
        for(int i=0; i < colorData.length; i++){
            Object data[] = colorData[i];
            boolean found=false;
            if (state != null)
            {
                for (int j=0; j < state.length; j++)
                {
                    if (((String) data[0]).indexOf(state[j]) == 0)                {
                        found = true;
                        break;
                    }
                }
            } else {
                found = true;
            }
            
            if (found)
            {
                Color c = null;
                String category = "";
                int size = 0;
                if (i % 3 == 0)
                {
                    c = color1;
                    category = "category1";
                    size = 10;
                } else if (i % 3 == 1)
                {
                    c = color2;
                    category = "category2";                
                    size = 12;
                } else
                {
                    c = color3;
                    category = "category3";                
                    size = 5;
                }
            
                this.add(
                    new SampleColorData.ColorData(
                        (String)data[0],
                        (String)data[0],
                        c,
                        category,
                        new Integer(size)));
            }
        }                
    }
    
    public SampleCountyData(long seed, int percentageFull) {
        Random random = new Random(seed);
        
        for(int i=0; i < colorData.length; i++){
            if ( random.nextInt(100) <= percentageFull ){
                Object data[] = colorData[i];
                int colorType = random.nextInt(6);
                Color color = SampleContinentData.colorSet[colorType];
                String category = SampleContinentData.categorySet[colorType];
                Integer value = random.nextInt(100);
                this.add(
                    new SampleColorData.ColorData(
                        (String)data[0],
                        (String)data[0],
                        color,
                        category,
                        value));
            }
        }
    }
    
